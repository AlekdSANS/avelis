function createImages(slug: string, name: string): ProductImage[] {
  const mainImage = heroImages[slug] ?? `/images/products/${slug}/main.webp`;

  return [
    {
      id: `${slug}-main`,
      url: mainImage,
      alt: `${name} fragrance presentation`,
      position: 0,
    },
    {
      id: `${slug}-detail-1`,
      url: `/images/products/${slug}/detail-1.webp`,
      alt: `${name} bottle and refill detail`,
      position: 1,
    },
    {
      id: `${slug}-detail-2`,
      url: `/images/products/${slug}/detail-2.webp`,
      alt: `${name} composition detail`,
      position: 2,
    },
  ];
}

function createNotes(
  slug: string,
  top: string[],
  heart: string[],
  base: string[],
): ProductNote[] {
  const group = (names: string[], type: FragranceNoteType) =>
    names.map((name, index) => ({
      id: `${slug}-${type.toLowerCase()}-${index + 1}`,
      name,
      type,
    }));

  return [...group(top, "TOP"), ...group(heart, "HEART"), ...group(base, "BASE")];
}

function createVariants(
  slug: string,
  prices: PriceSet,
  stock: Partial<Record<"b50" | "b100" | "r50" | "r100" | "r150", number>> = {},
  compareAt: Partial<Record<"b50" | "b100" | "r50" | "r100" | "r150", number>> = {},
): ProductVariant[] {
  const variant = (
    key: "b50" | "b100" | "r50" | "r100" | "r150",
    format: ProductVariant["format"],
    volumeMl: number,
    price: number,
  ): ProductVariant => ({
    id: `${slug}-${key}`,
    format,
    volumeMl,
    price,
    compareAtPrice: compareAt[key],
    sku: `AVL-${slug.slice(0, 4).toUpperCase()}-${format === "BOTTLE" ? "B" : "R"}${volumeMl}`,
    stock: stock[key] ?? 18,
  });

  return [
    variant("b50", "BOTTLE", 50, prices.bottle50),
    variant("b100", "BOTTLE", 100, prices.bottle100),
    variant("r50", "REFILL", 50, prices.refill50),
    variant("r100", "REFILL", 100, prices.refill100),
    variant("r150", "REFILL", 150, prices.refill150),
  ];
}

